    public void preencherTabelaCurso(String SQL){ //Preenche a Tabela de Cursos
    ArrayList dados = new ArrayList();
    String [] Colunas = new String[]{"Cod_Curso","Nome_Curso"};
    
    conecta.executaSQL(SQL);
        try {
            conecta.rs.first();
             do{ 
                 dados.add(new Object[]{conecta.rs.getInt("Cod_Curso"), conecta.rs.getString("Nome_Curso")});
    }while(conecta.rs.next());
        } catch (SQLException ex) {
             JOptionPane.showMessageDialog(rootPane,"Erro ao preencher a tabela");
        }

    ModeloTabela modelo = new ModeloTabela(dados, Colunas);
    tabelaCursos.setModel(modelo);
    tabelaCursos.getColumnModel().getColumn(0).setPreferredWidth(80);//indice 0 Cursos
    tabelaCursos.getColumnModel().getColumn(0).setResizable(false);//nao pode alterar o tamanho com o mouse
    tabelaCursos.getColumnModel().getColumn(1).setPreferredWidth(180);//indice 1 Cursos
    tabelaCursos.getColumnModel().getColumn(1).setResizable(false);//nao pode alterar o tamanho com o mouse
    tabelaCursos.getTableHeader().setReorderingAllowed(false);//reorganizar a alocaçao
    tabelaCursos.setAutoResizeMode(tabelaCursos.AUTO_RESIZE_OFF);//naopodera ser redimensionada;
    tabelaCursos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    
    }
