    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
        try{//BOTÃO DE INSERÇÃO
            PreparedStatement pst = conecta.conn.prepareStatement("INSERT INTO Curso (Cod_Curso,Nome_Curso) VALUES (?,?)");//passagem do SQL para inserção
            pst.setString(1,botCod_Curso.getText());//passagem de parametros
            pst.setString(2,NomeCurso.getText());
            pst.executeUpdate();//executa a inserção
            JOptionPane.showMessageDialog(rootPane,"Salvo com Sucesso!");
        }catch (SQLException ex){
            JOptionPane.showMessageDialog(rootPane,"Erro ao Salvar! \n ERRO: "+ex);
        }
        botCod_Curso.setEnabled(false);  
        NomeCurso.setEnabled(false);
        jButton16.setEnabled(false);
        jButton2.setEnabled(false);
        jButton3.setEnabled(false);
        jButton1.setEnabled(true);
        preencherTabelaCurso("select * from curso order by Cod_Curso");
        
         //Preenchimento dos Combo Box - CURSO
        conCurso.conexao();
        conCurso.executaSQL("select * from curso order by Cod_Curso");
        jComboBox3.removeAllItems();
        jComboBox4.removeAllItems();
        try{
            conCurso.rs.first();
                do{
                    jComboBox3.addItem(conCurso.rs.getString("Cod_Curso"));
                    jComboBox4.addItem(conCurso.rs.getString("Cod_Curso"));       
                }while(conCurso.rs.next());             
                }catch(SQLException ex){                   
                }
    }//GEN-LAST:event_jButton16ActionPerformed

